package IfElse;

public class TernaryVsIfElse {
    public static void main(String[] args) {

        // using ternary operator
        int number1;
        number1 = (10 > 0) ? 1 : 2;


        // using if-else
        int number2;
        if (10 > 0) {
            number2 = 1;
        } else {
            number2 = 2;
        }
    }
}

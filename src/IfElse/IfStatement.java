package IfElse;

public class IfStatement {
    public static void main(String[] args) {
        // sintaksa e pare (e preferuara)
        int a = 10;
        if(a > 0) {
            System.out.println(a);
        }
        System.out.println("Suksese");


        // sintaksa e dyte
        int b = 10;
        if(b > 0) System.out.println(a);
        System.out.println("Suksese"); // nuk eshte e varur nga "IF"
    }
}








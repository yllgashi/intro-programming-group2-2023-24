package datatypes;

// boolean
// range => true - false
public class BooleanExample {
    public static void main(String[] args) {

        // first example
        boolean firstBoolean;
        firstBoolean = true;

        // second example
        boolean secondBoolean, thirdBoolean;
        secondBoolean = false;
        thirdBoolean = true;


        // third example
        boolean fourthBoolean = true;


        boolean fifthBoolean = 1 > 2;

        // result
        System.out.println(firstBoolean);
        System.out.println(secondBoolean);
        System.out.println(thirdBoolean);
        System.out.println(fourthBoolean);
        System.out.println(fifthBoolean);
    }
}

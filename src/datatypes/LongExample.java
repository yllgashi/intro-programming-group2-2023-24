package datatypes;

// long
// rang => -9223372036854775808 to 9223372036854775807
// size => 64-bit
public class LongExample {
    public static void main(String[] args) {

        // first example
        long firstLong;
        firstLong = 10_000_000_000L;

        // second example
        long secondLong, thirdLong;
        secondLong = 20_000_000_000L;
        thirdLong = 30_000_000_000L;


        // third example
        long fourthLong = 40_000_000_000L;


        // check result
        System.out.println(firstLong);
        System.out.println(secondLong);
        System.out.println(thirdLong);
        System.out.println(fourthLong);
    }
}

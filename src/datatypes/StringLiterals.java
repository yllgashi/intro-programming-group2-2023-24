package datatypes;

public class StringLiterals {

    public static void main(String[] args) {
        char charOne = '\u2605';
        System.out.println(charOne);


        char charTwo = '\\';
        System.out.println(charTwo);

        char charThree = '\'';
        System.out.println(charThree);
        System.out.println("\"");


        System.out.println("Emri im eshte:\nYll Gashi");
        System.out.println("Emri im eshte:Yll Gashi");
        System.out.println("Emri im eshte:\tYll Gashi");
        System.out.println("Emri im eshte:\bYll Gashi");
    }
}

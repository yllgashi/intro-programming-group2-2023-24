package other_operators;

public class MathExample {
    public static void main(String[] args) {
        // Math
        System.out.println(Math.abs(-2)); // vleren absolute te nje INT
        System.out.println(Math.abs(-2.24)); // vleren absolute te nje DOUBLE
        System.out.println(Math.min(1, 2)); // vlera me e vogel e dhene
        System.out.println(Math.max(1, 2)); // vlera me e madhe

        System.out.println(Math.exp(2)); // eksponenciali
        System.out.println(Math.log(10)); // logaritmi
        System.out.println(Math.pow(2, 4)); // ngritja ne fuqi

        System.out.println(Math.round(10.4)); // rrumbullaksimi
        System.out.println(Math.round(10.6)); // rrumbullaksimi

        System.out.println(Math.random()); // vlere te rastesishme te rangut 0-1 (perfshihet 0, nuk perfshihet 1)


        System.out.println(Math.PI); // konstanta e PI
        System.out.println(Math.E); // konstanta e E







    }
}